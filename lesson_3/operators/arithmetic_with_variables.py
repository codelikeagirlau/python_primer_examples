num_1 = 4
num_2 = 6

# ADDITION
# 4 + 3 = 7
print(num_1 + num_2)

# SUBTRACTION
# 4 - 3 = 1
print(num_1 - num_2)

# MULTIPLICATION
# multiplication is written using * not x
# 4 * 3 = 12
print(num_1 * num_2)

# DIVISION
# division is written using /
# 4 / 3 = 1.333
print(num_1 / num_2)

# EXPONENTIATION
# exponentiation is written using **
# it is the same as 'power of'
print(num_1 ** num_2)

# MODULO
# modulo is written as %
# it is the same as 'the remainder' when one number is divided by another
# the output of the modulo operator is called the 'modulus', or 'remainder'
print(num_1 % num_2)

# FLOOR DIVISION
# when one number is divided by another, the floor division takes the nearest integer
# which is than less than or equal to the result
# it is the same as 'rounding down' the result of the division
print(num_1 // num_2)